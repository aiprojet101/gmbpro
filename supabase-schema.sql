  category TEXT NOT NULL,
  task_type TEXT NOT NULL,
  title TEXT NOT NULL,
  why TEXT,
  instructions TEXT,
  content_to_copy TEXT,
  priority INTEGER DEFAULT 1,
  status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'done', 'skipped')),
